        //
        // POST: /Account/ExternalLogin
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public ActionResult ExternalLogin(string provider, string returnUrl)
        {
            // Request a redirect to the external login provider
            return new ChallengeResult(provider, Url.Action("ExternalLoginCallback", "Authentication", new { ReturnUrl = returnUrl }));
        }

        //
        // GET: /Account/ExternalLoginCallback
        [AllowAnonymous]
        public async Task<ActionResult> ExternalLoginCallback(string returnUrl)
        {
            var loginInfo = await AuthenticationManager.GetExternalLoginInfoAsync();
            if (loginInfo == null)
            {
                return RedirectToAction("Login");
            }

            // Sign in the user with this external login provider if the user already has a login
            Account user;
            if (loginInfo.Login.LoginProvider == "Facebook")
            {
                user = account.GetByFacebook(long.Parse(loginInfo.Login.ProviderKey));
            }
            else
            {
                user = account.GetByTwitter(long.Parse(loginInfo.Login.ProviderKey));
            }
            
            if (user == null)
            {
                return RedirectToAction("NoAccount");
            }

            var result = await SignInManager.ExternalSignInAsync(loginInfo, isPersistent: false);
            if (result == SignInStatus.Failure)
            {
                SignInManager.AuthenticationManager.SignIn(await SignInManager.CreateUserIdentityAsync(user));
            }

            return RedirectToLocal(returnUrl);
        }

